void fill(unsigned char dat1,unsigned char dat2)
{
	unsigned char x,y;
	for(y=0;y<8;y++)
	{
		Write_Command(0xb0+y);
		Write_Command(0x02);
		Write_Command(0x10);
		for(x=0;x<132;x++)
		{
			Write_Data(dat1);
			Write_Data(dat2);
		}
	}
	Delay(1);
}
